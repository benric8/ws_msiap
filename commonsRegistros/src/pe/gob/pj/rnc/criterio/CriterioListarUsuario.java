package pe.gob.pj.rnc.criterio;


public class CriterioListarUsuario {
	
	
	private Object nombres;
	private Object apellidos;	
	private Object codEntidadExterna;
	private Object codPerfil;
	private Object codDistritoJudicial;
	
	
	
	public Object getApellidos() {
		return apellidos;
	}
	public void setApellidos(Object apellidos) {
		this.apellidos = apellidos;
	}
	public Object getCodEntidadExterna() {
		return codEntidadExterna;
	}
	public void setCodEntidadExterna(Object codEntidadExterna) {
		this.codEntidadExterna = codEntidadExterna;
	}
	public Object getCodPerfil() {
		return codPerfil;
	}
	public void setCodPerfil(Object codPerfil) {
		this.codPerfil = codPerfil;
	}
	public Object getNombres() {
		return nombres;
	}
	public void setNombres(Object nombres) {
		this.nombres = nombres;
	}
	public Object getCodDistritoJudicial() {
		return codDistritoJudicial;
	}
	public void setCodDistritoJudicial(Object codDistritoJudicial) {
		this.codDistritoJudicial = codDistritoJudicial;
	}
	
	
	

}
