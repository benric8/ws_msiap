
package pe.gob.pj.rnc.model;

public class DelitoReporte
{
	private String codg_delit;
	private String desc_delit;
	private Integer numr_delitos;

	public String getCodg_delit() {
		return codg_delit;
	}
	public void setCodg_delit(String codg_delit) {
		this.codg_delit = codg_delit;
	}
	public String getDesc_delit() {
		return desc_delit;
	}
	public void setDesc_delit(String desc_delit) {
		this.desc_delit = desc_delit;
	}
	public Integer getNumr_delitos() {
		return numr_delitos;
	}
	public void setNumr_delitos(Integer numr_delitos) {
		this.numr_delitos = numr_delitos;
	}

}
