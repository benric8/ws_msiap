package pe.gob.pj.rnc.model.reniec;


public class PaginacionReniec {
	private int size;
	private int page;
	
	
	
	public PaginacionReniec() {
		super();
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	
	
}
