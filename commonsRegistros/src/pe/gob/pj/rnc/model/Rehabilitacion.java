package pe.gob.pj.rnc.model;



public class Rehabilitacion {

	private String CANCELADO;
	
	private String DESC_INSTANCIA;
	
	private String FECHA_REHAB;

	public String getCANCELADO() {
		return CANCELADO;
	}

	public void setCANCELADO(String cancelado) {
		CANCELADO = cancelado;
	}

	public String getDESC_INSTANCIA() {
		return DESC_INSTANCIA;
	}

	public void setDESC_INSTANCIA(String desc_instancia) {
		DESC_INSTANCIA = desc_instancia;
	}

	public String getFECHA_REHAB() {
		return FECHA_REHAB;
	}

	public void setFECHA_REHAB(String fecha_rehab) {
		FECHA_REHAB = fecha_rehab;
	}
	
	
	
}
